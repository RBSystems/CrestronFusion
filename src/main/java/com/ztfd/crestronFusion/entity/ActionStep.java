package com.ztfd.crestronFusion.entity;

public class ActionStep {
    private String analogValue;
    private String attributeID;
    private String attributeType;
    private String digitalValue;
    private String lastModified;
    private String orderIndex;
    private String serialValue;

    public String getAnalogValue() {
        return analogValue;
    }

    public void setAnalogValue(String analogValue) {
        this.analogValue = analogValue;
    }

    public String getAttributeID() {
        return attributeID;
    }

    public void setAttributeID(String attributeID) {
        this.attributeID = attributeID;
    }

    public String getAttributeType() {
        return attributeType;
    }

    public void setAttributeType(String attributeType) {
        this.attributeType = attributeType;
    }

    public String getDigitalValue() {
        return digitalValue;
    }

    public void setDigitalValue(String digitalValue) {
        this.digitalValue = digitalValue;
    }

    public String getLastModified() {
        return lastModified;
    }

    public void setLastModified(String lastModified) {
        this.lastModified = lastModified;
    }

    public String getOrderIndex() {
        return orderIndex;
    }

    public void setOrderIndex(String orderIndex) {
        this.orderIndex = orderIndex;
    }

    public String getSerialValue() {
        return serialValue;
    }

    public void setSerialValue(String serialValue) {
        this.serialValue = serialValue;
    }
}
