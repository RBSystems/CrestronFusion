package com.ztfd.crestronFusion.entity;

public class PresetField {
    private String fieldLabel;
    private String fieldSlot;
    private String fieldValue;
    private String maximumFields;

    public String getFieldLabel() {
        return fieldLabel;
    }

    public void setFieldLabel(String fieldLabel) {
        this.fieldLabel = fieldLabel;
    }

    public String getFieldSlot() {
        return fieldSlot;
    }

    public void setFieldSlot(String fieldSlot) {
        this.fieldSlot = fieldSlot;
    }

    public String getFieldValue() {
        return fieldValue;
    }

    public void setFieldValue(String fieldValue) {
        this.fieldValue = fieldValue;
    }

    public String getMaximumFields() {
        return maximumFields;
    }

    public void setMaximumFields(String maximumFields) {
        this.maximumFields = maximumFields;
    }
}
